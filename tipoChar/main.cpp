#include <iostream>

using namespace std;

int main() {
    /**
        el caracter se representa con las comillas simples ''
        la cadena (string) se representa con las comillas dobles ""
    */
    char val = 'a';
    int valNum = static_cast<int>(val);
    cout << val << " su valor en numero es: " << valNum << endl;
    return 0;
}
